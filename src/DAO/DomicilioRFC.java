package DAO;

public class DomicilioRFC {
	private long idDireccionRFC;
	private String calle;
	private int numeroint;
	private int numeroext;
	private String razonsocial;
	private int cp;
	private String delegacion;
	private String email;
	
	public long getIdDireccionRFC() {
		return idDireccionRFC;
	}
	public void setIdDireccionRFC(long idDireccionRFC) {
		this.idDireccionRFC = idDireccionRFC;
	}
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public int getNumeroint() {
		return numeroint;
	}
	public void setNumeroint(int numeroint) {
		this.numeroint = numeroint;
	}
	public int getNumeroext() {
		return numeroext;
	}
	public void setNumeroext(int numeroext) {
		this.numeroext = numeroext;
	}
	public String getRazonsocial() {
		return razonsocial;
	}
	public void setRazonsocial(String razonsocial) {
		this.razonsocial = razonsocial;
	}
	public int getCp() {
		return cp;
	}
	public void setCp(int cp) {
		this.cp = cp;
	}
	public String getDelegacion() {
		return delegacion;
	}
	public void setDelegacion(String delegacion) {
		this.delegacion = delegacion;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	

}
